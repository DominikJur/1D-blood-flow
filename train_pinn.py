"""
PINN training for 1D blood flow bifurcation (parent + 2 daughters)
Uses DeepXDE + PyTorch.

Trains on data from blood_flow_data.csv (generated by solver.py).
Physics: 1D continuity + momentum + tube law.
Boundary conditions based on:
  D. Kim, J. Tithof. "One-Dimensional Modeling of Blood Flow:
  A Comprehensive Yet Concise Review" (2025) - sekcje 6, 7
"""

